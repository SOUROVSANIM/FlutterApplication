import 'package:flutter/material.dart';
import 'package:image_picker/image_picker.dart';
import 'dart:io';

class ProfileScreen extends StatefulWidget {
  const ProfileScreen({Key? key}) : super(key: key);

  @override
  _ProfileScreenState createState() => _ProfileScreenState();
}

class _ProfileScreenState extends State<ProfileScreen> {
  XFile? _imageFile;
  final _globalkey = GlobalKey<FormState>();
  TextEditingController _name = TextEditingController();
  TextEditingController _profession = TextEditingController();
  TextEditingController _dob = TextEditingController();
  TextEditingController _title = TextEditingController();
  TextEditingController _about = TextEditingController();
  final ImagePicker _picker = ImagePicker();

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: Padding(
        padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 30),
        child: ListView(
          children: <Widget>[
            imageProfile(),
            SizedBox(
              height: 20,
            ),
            nameTextField(),
            SizedBox(
              height: 20,
            ),
            professionTextField(),
            SizedBox(
              height: 20,
            ),
            dobField(),
            SizedBox(
              height: 20,
            ),
            titleTextField(),
            SizedBox(
              height: 20,
            ),
            aboutTextField(),
            SizedBox(
              height: 20,
            ),
          ],
        ),
      ),
    );
  }

  Widget imageProfile() {
    final userData = FirebaseFirestore.instance
        .collection('users')
        .where('uid', isEqualTo: FirebaseAuth.instance.currentUser?.uid)
        .limit(1)
        .get();
    return Center(
      child: Stack(children: <Widget>[
        FutureBuilder(
          future: userData,
          builder: (context, snapshot) {
            final data = snapshot.data?.docs.first.data();

            if (data == null || data.containsKey('image_url') == false) {
              CircleAvatar(
                radius: 80.0,
                backgroundImage: _imageFile == null
                    ? AssetImage("assets/logo1.jpeg")
                    : FileImage(File(_imageFile!.path)) as ImageProvider,
              );
            }

            return CircleAvatar(
              radius: 80.0,
              backgroundImage: _imageFile == null
                  ? NetworkImage(data!['image_url'])
                  : FileImage(File(_imageFile!.path)) as ImageProvider,
            );
          },
        ),
        Positioned(
          bottom: 20.0,
          right: 20.0,
          child: InkWell(
            onTap: () {
              showModalBottomSheet(
                context: context,
                builder: ((builder) => bottomSheet()),
              );
            },
            child: Icon(
              Icons.camera_alt,
              color: Colors.teal,
              size: 28.0,
            ),
          ),
        ),
      ]),
    );
  }

  Widget bottomSheet() {
    return Container(
      height: 100.0,
      width: MediaQuery.of(context).size.width,
      margin: EdgeInsets.symmetric(
        horizontal: 20,
        vertical: 20,
      ),
      child: Column(
        children: <Widget>[
          Text(
            "Choose Profile photo",
            style: TextStyle(
              fontSize: 20.0,
            ),
          ),
          SizedBox(
            height: 20,
          ),
          Row(mainAxisAlignment: MainAxisAlignment.center, children: <Widget>[
            TextButton.icon(
              icon: Icon(Icons.camera),
              onPressed: () {
                takePhoto(ImageSource.camera);
              },
              label: Text("Camera"),
            ),
            TextButton.icon(
              icon: Icon(Icons.image),
              onPressed: () {
                takePhoto(ImageSource.gallery);
              },
              label: Text("Gallery"),
            ),
          ])
        ],
      ),
    );
  }

  void takePhoto(ImageSource source) async {
    final pickedFile = await _picker.pickImage(
      source: source,
    );
    setState(() {
      _imageFile = pickedFile;
    });

    // Upload image here
    if (mounted && _imageFile != null) {
      final storage = FirebaseStorage.instance;
      final fileRef = storage.ref().child(
          '/image/${_imageFile?.name ?? ''}${DateTime.now().millisecondsSinceEpoch}');
      final bytes = await _imageFile?.readAsBytes();
      if (bytes != null) {
        await fileRef.putData(bytes);

        final imgLink = await fileRef.getDownloadURL();
        final firestore = FirebaseFirestore.instance;
        final users = await firestore
            .collection('users')
            .where('uid', isEqualTo: FirebaseAuth.instance.currentUser?.uid)
            .limit(1)
            .get();
        if (users.docs.isNotEmpty) {
          final user = users.docs.first;
          final userData = user.data();
          await firestore
              .collection('users')
              .doc(user.id)
              .set({...userData, 'image_url': imgLink});
        }
      }
    }
  }

  Widget nameTextField() {
    return TextFormField(
      controller: _name,
      validator: (value) {
        if (value == null || value.isEmpty) return "Name can't be empty";
        return null;
